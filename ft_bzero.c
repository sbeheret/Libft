#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptr;

	if (n != 0)
	{
		i = 0;
		ptr = s;
		while (i < n)
		{
			ptr[i] = 0;
			i++;
		}
	}
}
