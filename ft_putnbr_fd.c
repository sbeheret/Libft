#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	mod;

	mod = n % 10;
	if (n == IMIN)
		n++;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(ABS(mod) + 48, fd);
}
