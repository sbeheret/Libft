#include "libft.h"

void	ft_putnbr(int n)
{
	int	mod;

	mod = n % 10;
	if (n == IMIN)
		n++;
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar(ABS(mod) + 48);
}
